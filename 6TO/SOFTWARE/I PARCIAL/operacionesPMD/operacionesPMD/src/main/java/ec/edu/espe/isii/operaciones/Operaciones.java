package ec.edu.espe.isii.operaciones;

/**
 *
 * @author Labs-DECC
 */
public class Operaciones {
    
    int suma(final int numero1, final int numero2) {
        return numero1 + numero2;
    }

    int resta(final int numero1, final int numero2) {
        return numero1 - numero2;
    }

    int multi(final int numero1, final int numero2) {
        return numero1 * numero2;
    }

    float division(final float numero1, final float numero2) {
        return numero1 / numero2;
    }
}
